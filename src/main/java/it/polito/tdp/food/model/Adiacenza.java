package it.polito.tdp.food.model;

public class Adiacenza {
	
	private String v1;
	private Double peso;
	
	
	public Adiacenza(String v1, Double peso) {
		super();
		this.v1 = v1;
		this.peso = peso;
	}
	public String getV1() {
		return v1;
	}
	public void setV1(String v1) {
		this.v1 = v1;
	}
	public Double getPeso() {
		return peso;
	}
	public void setPeso(Double peso) {
		this.peso = peso;
	}
	
	
	
	
}
